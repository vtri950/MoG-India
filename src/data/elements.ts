// Merge state elements and local govt / civil services into the main array
elements.push(...stateElements, ...localGovtElements);

// Create a map for quick element lookup
const elementMap = new Map<string, GovElement>();
elements.forEach(el => elementMap.set(el.id, el));

// Compute reverse lookups (children)
const childrenMap = new Map<string, string[]>();
const secondaryChildrenMap = new Map<string, string[]>();

elements.forEach(el => {
  el.parentIds.forEach(parentId => {
    if (!childrenMap.has(parentId)) {
      childrenMap.set(parentId, []);
    }
    childrenMap.get(parentId)!.push(el.id);
  });
  
  el.secondaryParentIds?.forEach(parentId => {
    if (!secondaryChildrenMap.has(parentId)) {
      secondaryChildrenMap.set(parentId, []);
    }
    secondaryChildrenMap.get(parentId)!.push(el.id);
  });
});

/**
 * Get an element by ID
 */
export function getElementById(id: string): GovElement | undefined {
  return elementMap.get(id);
}

/**
 * Get all elements
 */
export function getAllElements(): GovElement[] {
  return elements;
}

/**
 * Get child element IDs (elements that have this element as a parent)
 */
export function getChildIds(id: string): string[] {
  return childrenMap.get(id) || [];
}

/**
 * Get secondary child element IDs
 */
export function getSecondaryChildIds(id: string): string[] {
  return secondaryChildrenMap.get(id) || [];
}

/**
 * Get all connected elements for an element
 */
export function getConnectedElements(id: string): {
  parents: GovElement[];
  children: GovElement[];
  secondaryParents: GovElement[];
  secondaryChildren: GovElement[];
} {
  const element = getElementById(id);
  if (!element) {
    return { parents: [], children: [], secondaryParents: [], secondaryChildren: [] };
  }
  
  return {
    parents: element.parentIds.map(pid => getElementById(pid)).filter((e): e is GovElement => e !== undefined),
    children: getChildIds(id).map(cid => getElementById(cid)).filter((e): e is GovElement => e !== undefined),
    secondaryParents: (element.secondaryParentIds || []).map(pid => getElementById(pid)).filter((e): e is GovElement => e !== undefined),
    secondaryChildren: getSecondaryChildIds(id).map(cid => getElementById(cid)).filter((e): e is GovElement => e !== undefined),
  };
}

/**
 * Get elements by category
 */
export function getElementsByCategory(category: string): GovElement[] {
  return elements.filter(el => el.category === category);
}

/**
 * Get elements by tag
 */
export function getElementsByTag(tagId: string): GovElement[] {
  return elements.filter(el => el.tags?.includes(tagId));
}

/**
 * Search elements by name or description
 */
export function searchElements(query: string): GovElement[] {
  const lowerQuery = query.toLowerCase();
  return elements.filter(el => 
    el.name.toLowerCase().includes(lowerQuery) ||
    el.nameHindi?.includes(query) ||
    el.abbreviation?.toLowerCase().includes(lowerQuery) ||
    el.description.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get a tag definition by ID
 */
export function getTagById(tagId: string): TagDefinition | undefined {
  return tagDefinitions.find(t => t.id === tagId);
}
